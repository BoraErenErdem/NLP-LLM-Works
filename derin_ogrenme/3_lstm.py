max_seq_len = max(len(i) for i in input_sequence)
input_sequence = pad_sequences(input_sequence, maxlen=max_seq_len)

X = input_sequence[:, :-1]
y = input_sequence[:, -1]
y = to_categorical(y, num_classes=total_words)

embedding_dim = 100
inputs = Input(shape=(max_seq_len - 1,))
embedding = Embedding(input_dim=total_words, output_dim=embedding_dim)(inputs)
lstm = LSTM(100, return_sequences=False)(embedding)
outputs = Dense(total_words, activation='softmax')(lstm)
model = Model(inputs=inputs, outputs=outputs)
model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])
model.summary()
model.fit(X, y, epochs=100, batch_size=64, verbose=1)
test_loss, test_acc = model.evaluate(X, y)
print(f'test_loss: {test_loss:.3f}, test_acc: {test_acc:.3f}')

def text_generator(text, next_words):
    for _ in range(next_words):
        token_list = tokenizer.texts_to_sequences([text])[0]
        token_list = pad_sequences([token_list], maxlen=max_seq_len - 1)
        predict = model.predict(token_list, verbose=0)
        predict_word_index = np.argmax(predict, axis=-1)
        predict_word = tokenizer.index_word[predict_word_index[0]]
        text = text + ' ' + predict_word
    return text

