tokenizer = Tokenizer()
tokenizer.fit_on_texts(texts) # vocabulary oluşturur ve her kelimeyi benzersiz index'e atar
total_words = len(tokenizer.word_index) + 1 # toplam kelime sayısı
# +1 eklenmesinin sebebi word_index indexleri 1'den başlar. normalde indexler 0'dan başladığından eşitlemek için +1 eklenir..!

# n-gram dizileri oluştur. # örn. bugün hava çok güzel. -> input: bugün hava -> hedef: çok vb.
input_sequences = []
for text in texts:
    token_list = tokenizer.texts_to_sequences([text])[0] # cümleyi sayısal indexlere çevirir
    for i in range(1, len(token_list)): # her adımda bir kelime daha ekleyerek n-grams dizileri oluşturur..!
        n_gram_sequences = token_list[:i + 1]
        input_sequences.append(n_gram_sequences)

max_sequence_length = max(len(i) for i in input_sequences) # en uzun dizinin uzunluğu

input_sequences = pad_sequences(input_sequences, max_sequence_length, padding='pre') # padding ile tüm dizileri aynı uzunluğa getirme

X = input_sequences[:, : -1] # son kelime hariç tüm kelimeler girdi
y = input_sequences[:, -1] # son kelime çıktı

y = to_categorical(y, num_classes=total_words) # one hot encoding

embedding_dim = 50
inputs = Input(shape=(max_sequence_length - 1,)) # X'in son kelime harici bütün kelimeleri almasından dolayı -1 gelir
embedding = Embedding(input_dim=total_words, output_dim=embedding_dim)(inputs)
x = LSTM(100, return_sequences=False)(embedding)
outputs = Dense(total_words, activation='softmax')(x)
model = Model(inputs, outputs)
model.compile(optimizer='adam', loss='categorical_crossentropy', metrics=['accuracy'])
model.summary()
history = model.fit(X, y, epochs=100, batch_size=32, verbose=1)

test_loss, test_acc = model.evaluate(X, y)
print(f'test loss: {test_loss:.3f}, test accuracy: {test_acc*100:.2f}%')

# custom function ile metin üretimi
def generate_text(text, next_words): # text -> başlangıç metni  # next_words -> kaç kelime üreteceği
    for _ in range(next_words):
        token_list = tokenizer.texts_to_sequences([text])[0] # girdi metnini sayısal verilere dönüştür
        token_list = pad_sequences([token_list], maxlen=max_sequence_length - 1, padding='pre') # padding ile uzunluk sabitlenir
        predict = model.predict(token_list, verbose=0)
        predict_word_index = np.argmax(predict, axis=-1) # en yüksek olasılığa sahip kelimenin indexini bul
        predicted_word = tokenizer.index_word[predict_word_index[0]] # indexi kelimeye çevir
        text = text + ' ' + predicted_word # kelimeyi metne ekle
    return text

